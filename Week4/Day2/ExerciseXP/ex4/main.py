# floats are floating point values which are decimals
# difference: intergers are whole numbers
# other way to generate float value
z = 0 # start value
u = 100
while z <= u: # y end value
    z += 0.1 # 0.1 step value
    print('%.1f' % z)
#creating list
arr = []
x = 1.5 # start value
y = 5.0
while x <= y: # y end value
    x += 0.5 # 0.5 step value
    arr.append(x)
print(arr)